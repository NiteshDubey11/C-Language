/*output using format specifier
%d for integer
%f for float
%c for character*/


#include <stdio.h>

int main()
{
    int age=18;
    char letter='n';
    float pi=3.14;
    printf("the age is %d\n",age);
    printf("the letter is %c\n",letter);
    printf("the real number is %f\n",pi);
    

    return 0;
}
